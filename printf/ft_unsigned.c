#include "ft_printf.h"

void	ft_unsigned(unsigned int n, int *len)
{
	if (n > 9)
	{
		ft_unsigned(n / 10, len);
		ft_putchar(n % 10 + '0');
	}
	else
		ft_putchar(n + '0');
	(*len)++;
}
